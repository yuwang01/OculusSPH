char* readShader(char* filename)
{
    FILE* file = fopen(filename, "r");
    if (!file)
    {
        fprintf(stderr, "Cannot open file %s\n", filename);
        exit(1);
    }

    char* shaderStr;
    long fileLen;

    fseek(file, 0, SEEK_END);
    fileLen = ftell(file);
    rewind(file);
    shaderStr = (char*)malloc((fileLen + 1) * sizeof(char));
    fread(shaderStr, sizeof(char), fileLen, file);
    fclose(file);
    shaderStr[fileLen] = 0;

    return shaderStr;
}

void fps(GLFWwindow* window) {  
    double current_seconds = glfwGetTime();
    double elapsed_seconds = current_seconds - previous_seconds;
    if (elapsed_seconds > .25) {
        previous_seconds = current_seconds;
        double FPS = (double)frame_count/elapsed_seconds;
        char tmp[128];

        sprintf(tmp, "fps: %.2f", FPS);
        glfwSetWindowTitle(window, tmp);
        frame_count = 0;
    }
    frame_count++;
}

int file_to_string(const char* file_name, char** result_string, size_t* string_len)
{
    int fd;
    unsigned file_len;
    struct stat file_status;
    int ret;

    *string_len = 0;
    fd = open(file_name, O_RDONLY);
    if (fd == -1)
    {
        printf("Error opening file %s\n", file_name);
        return -1;
    }
    ret = fstat(fd, &file_status);
    if (ret)
    {
        printf("Error reading status for file %s\n", file_name);
        return -1;
    }
    file_len = file_status.st_size;

    *result_string = (char*)calloc(file_len + 1, sizeof(char));
    ret = read(fd, *result_string, file_len);
    if (!ret)
    {
        printf("Error reading from file %s\n", file_name);
        return -1;
    }

    close(fd);

    *string_len = file_len;
    return 0;
}

